# Figure 6: Reward decomposition stacked bar
# ---------------------------------------------------------------------------
def fig6_reward_decomposition(all_data):
    """Three-way reward decomposition at final train step.

    reward = correct + 0.15*(boxed-1) + 0.15*(eos-1)
    """
    fig, ax = plt.subplots(figsize=(8, 5))
    models = list(all_data.keys())
    x = np.arange(len(models))
    width = 0.6
    FORMAT_COEF = 0.15  # v3b hyperparameter
    EOS_COEF = 0.15

    correct_vals, boxed_pen, eos_pen, reward_vals = [], [], [], []
    for name in models:
        train, _ = all_data[name]
        last = train[-1]
        c = last["env/all/correct"]
        b = last["env/all/format_boxed"]
        e = last["env/all/format_eos"]
        r = last["env/all/reward/total"]
        correct_vals.append(c)
        boxed_pen.append(FORMAT_COEF * (1 - b))
        eos_pen.append(EOS_COEF * (1 - e))
        reward_vals.append(r)

    correct_arr = np.array(correct_vals)
    boxed_arr = np.array(boxed_pen)
    eos_arr = np.array(eos_pen)
    reward_arr = np.array(reward_vals)

    ax.bar(x, reward_arr, width, label="Net reward", color=CB_GREEN, edgecolor="white")
    ax.bar(x, boxed_arr, width, bottom=reward_arr,
           label=f"Box penalty (coef={FORMAT_COEF})", color=CB_ORANGE, alpha=0.8, edgecolor="white")
    ax.bar(x, eos_arr, width, bottom=reward_arr + boxed_arr,
           label=f"EOS penalty (coef={EOS_COEF})", color=CB_RED, alpha=0.7, edgecolor="white")

    for i in range(len(models)):
        ax.text(i, correct_arr[i] + 0.01, f"correct={correct_arr[i]:.1%}", ha="center", fontsize=8)
        ax.text(i, reward_arr[i] / 2, f"r={reward_arr[i]:.3f}", ha="center", fontsize=8,
                color="white", fontweight="bold")

    ax.set_xticks(x)
    ax.set_xticklabels(models, fontsize=9)
    ax.set_ylabel("Value")
    ax.set_title("Final Train Step: Reward = correct + penalties")
    ax.legend(fontsize=8)
    ax.grid(axis="y", alpha=0.3)
    save(fig, "06_reward_decomposition", fig6_reward_decomposition)


