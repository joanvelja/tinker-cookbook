# Figure 9: Grader false negative rate by model
# ---------------------------------------------------------------------------
def fig9_grader_accuracy(all_data):
    """Per-model grader false negative rates from experiment-log-session2.md.

    Actual measured FN rates on wrong-but-boxed answers:
    - GPT-OSS v3b: 14% (1/7)
    - Instruct v3b: 24% (5/21)
    - Think v3b: had a separate bug (\\boxed{answer} literal accepted)
    """
    fig, ax = plt.subplots(figsize=(7, 5))

    models = ["GPT-OSS-20B", "Qwen3-30B-Instruct", "Qwen3-30B-Think"]
    fn_counts = [1, 5]
    sample_sizes = [7, 21, None]
    fn_rates = [fn_counts[0] / sample_sizes[0], fn_counts[1] / sample_sizes[1], np.nan]
    colors = [MODEL_COLORS[m] for m in models]

    x = np.arange(len(models))
    bars = ax.bar(x[:2], fn_rates[:2], width=0.5, color=colors[:2], edgecolor="white")

    for i, bar in enumerate(bars):
        n = sample_sizes[i]
        c = fn_counts[i]
        ax.text(bar.get_x() + bar.get_width() / 2, bar.get_height() + 0.01,
                f"{c/n:.0%} ({c}/{n})", ha="center", fontsize=10, fontweight="bold")

    ax.bar(x[2], 0.02, width=0.5, color=colors[2], edgecolor="white", alpha=0.4)
    ax.text(x[2], 0.05, "grader bug:\n\\boxed{{answer}}\nliteral accepted",
            ha="center", fontsize=8, fontstyle="italic", color=CB_RED)

    ax.set_xticks(x)
    ax.set_xticklabels(models, fontsize=9)
    ax.set_ylabel("False negative rate (wrong answers graded correct)")
    ax.set_title("LLM Grader False Negative Rate by Model")
    ax.set_ylim(0, 0.35)
    ax.grid(axis="y", alpha=0.3)

    ax.text(0.98, 0.62,
            "Common FN causes:\n"
            "  LaTeX \\text{{}} wrapping\n"
            "  Variable name diffs\n"
            "  Double-boxing\n\n"
            "Small n (7, 21) \u2014\n"
            "rates are indicative",
            transform=ax.transAxes, ha="right", va="top", fontsize=8,
            color="gray", family="monospace",
            bbox=dict(boxstyle="round,pad=0.3", facecolor="whitesmoke", edgecolor="lightgray"))
    save(fig, "09_grader_accuracy", fig9_grader_accuracy)


# ---------------------------------------------------------------------------
# Figure 10: Train vs eval gap over time per model
# ---------------------------------------------------------------------------
def fig10_train_eval_gap(all_data):
    fig, ax = plt.subplots(figsize=(8, 5))
    for name, (train, evl) in all_data.items():
        color = MODEL_COLORS[name]
        eval_steps = [d["step"] for d in evl]
        eval_correct = {d["step"]: d["test/test/env/all/correct"] for d in evl}
        train_correct = {d["step"]: d["env/all/correct"] for d in train}

        gaps = []
        steps_plot = []
        for step in eval_steps:
            if step in train_correct:
                gap = eval_correct[step] - train_correct[step]
                gaps.append(gap)
                steps_plot.append(step)

        if steps_plot:
            ax.plot(steps_plot, gaps, color=color, marker=MODEL_MARKERS[name],
                    linewidth=2, markersize=7, label=name)

    ax.axhline(0, color="gray", linewidth=0.8, linestyle=":")
    ax.set_xlabel("Training step")
    ax.set_ylabel("Eval \u2212 Train accuracy gap")
    ax.set_title("Generalization Gap Over Training")
    ax.legend()
    ax.grid(axis="y", alpha=0.3)

    ax.text(0.98, 0.95, "positive gap expected (eval uses lower temp)",
            transform=ax.transAxes, ha="right", va="top", fontsize=8, color="gray")
    ax.text(0.98, 0.05, "negative gap = eval-specific failure",
            transform=ax.transAxes, ha="right", va="bottom", fontsize=8, color="gray")
    save(fig, "10_train_eval_gap", fig10_train_eval_gap)


