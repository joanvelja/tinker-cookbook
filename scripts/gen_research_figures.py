# Figure 1: Eval trajectory comparison (correct over steps)
# ---------------------------------------------------------------------------
def fig1_eval_trajectory(all_data):
    fig, ax = plt.subplots(figsize=(8, 5))
    for name, (train, evl) in all_data.items():
        steps = [d["step"] for d in evl]
        correct = [d["test/test/env/all/correct"] for d in evl]
        ax.plot(steps, correct, color=MODEL_COLORS[name], marker=MODEL_MARKERS[name],
                linewidth=2, markersize=7, label=name)
        # Annotate peak
        peak_idx = int(np.argmax(correct))
        ax.annotate(f"peak {correct[peak_idx]:.1%}",
                     xy=(steps[peak_idx], correct[peak_idx]),
                     xytext=(0, 12), textcoords="offset points",
                     fontsize=8, ha="center", color=MODEL_COLORS[name],
                     arrowprops=dict(arrowstyle="-", color=MODEL_COLORS[name], lw=0.8))
    ax.set_xlabel("Training step")
    ax.set_ylabel("Eval accuracy (fraction correct)")
    ax.set_title("Eval Accuracy Over Training")
    ax.legend()
    ax.set_ylim(0, 0.75)
    ax.grid(axis="y", alpha=0.3)
    save(fig, "01_eval_trajectory", fig1_eval_trajectory)


# ---------------------------------------------------------------------------
# Figure 2: P(correct|boxed) decomposition over eval points
# ---------------------------------------------------------------------------
def fig2_pcorrect_given_boxed(all_data):
    fig, axes = plt.subplots(1, 3, figsize=(14, 4.5), sharey=True)
    for ax, (name, (train, evl)) in zip(axes, all_data.items()):
        steps = [d["step"] for d in evl]
        correct = np.array([d["test/test/env/all/correct"] for d in evl])
        boxed = np.array([d["test/test/env/all/format_boxed"] for d in evl])
        p_corr_given_boxed = np.where(boxed > 0, correct / boxed, 0)

        ax.plot(steps, boxed, color=CB_SKY, marker="s", linewidth=2, label="P(boxed)")
        ax.plot(steps, correct, color=MODEL_COLORS[name], marker="o", linewidth=2, label="P(correct)")
        ax.plot(steps, p_corr_given_boxed, color=CB_ORANGE, marker="^", linewidth=2,
                linestyle="--", label="P(correct|boxed)")
        ax.set_xlabel("Training step")
        ax.set_title(name)
        ax.legend(fontsize=8)
        ax.set_ylim(0, 1.05)
        ax.grid(axis="y", alpha=0.3)
    axes[0].set_ylabel("Probability")
    fig.suptitle("Eval: Decomposing Correctness via Format Compliance", fontsize=12, y=1.02)
    fig.tight_layout()
    save(fig, "02_pcorrect_decomposition", fig2_pcorrect_given_boxed)


