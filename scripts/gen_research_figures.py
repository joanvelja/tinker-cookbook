"""Generate all publication-quality figures for the RLVR-Maths research report.

Reads raw metrics.jsonl from each v3b run and produces 10 figures as both
PNG (600 dpi) and PDF, using serif fonts and the Wong colorblind-safe palette.

Also saves per-figure reproduction scripts to reports/figures/.

Usage:
    uv run python scripts/gen_research_figures.py
"""

