# Figure 5: Entropy comparison bar chart (initial vs final for each model)
# ---------------------------------------------------------------------------
def fig5_entropy_comparison(all_data):
    fig, ax = plt.subplots(figsize=(7, 5))
    models = list(all_data.keys())
    x = np.arange(len(models))
    width = 0.35

    initial_entropy = []
    final_entropy = []
    for name in models:
        train, _ = all_data[name]
        entropies = [d["optim/entropy"] for d in train if "optim/entropy" in d]
        initial_entropy.append(entropies[0])
        final_entropy.append(entropies[-1])

    bars1 = ax.bar(x - width / 2, initial_entropy, width, label="Initial", color=CB_SKY, edgecolor="white")
    bars2 = ax.bar(x + width / 2, final_entropy, width, label="Final", color=CB_BLUE, edgecolor="white")

    for bar in bars1:
        ax.text(bar.get_x() + bar.get_width() / 2, bar.get_height() + 0.01,
                f"{bar.get_height():.3f}", ha="center", va="bottom", fontsize=8)
    for bar in bars2:
        ax.text(bar.get_x() + bar.get_width() / 2, bar.get_height() + 0.01,
                f"{bar.get_height():.3f}", ha="center", va="bottom", fontsize=8)

    ax.set_xticks(x)
    ax.set_xticklabels(models, fontsize=9)
    ax.set_ylabel("Entropy")
    ax.set_title("Policy Entropy: Start vs End of Training")
    ax.legend()
    ax.grid(axis="y", alpha=0.3)
    save(fig, "05_entropy_comparison", fig5_entropy_comparison)


