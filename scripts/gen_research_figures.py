# ---------------------------------------------------------------------------
# Data loading
# ---------------------------------------------------------------------------
def load_metrics(path: Path) -> list[dict]:
    with open(path) as f:
        return [json.loads(line) for line in f]


def split_train_eval(data: list[dict]):
    """Split into train rows (have env/all/correct) and eval rows (have test/test/...)."""
    train = [d for d in data if "env/all/correct" in d]
    evl = [d for d in data if "test/test/env/all/correct" in d]
    return train, evl


def load_all():
    """Return dict[model_name] -> (train_rows, eval_rows)."""
    result = {}
    for name, path in RUNS.items():
        data = load_metrics(path)
        result[name] = split_train_eval(data)
    return result


